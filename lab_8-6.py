# the function
def factorial(num):
    hi = 0
    LOL = 0
    bob = 0
    while LOL < num:
        jah = num - hi
        bob += jah
        hi += 1
        LOL += 1
    return bob
        
print(factorial(1))
print(factorial(2))
print(factorial(3))
print(factorial(4))
print(factorial(10))
print(factorial(100))
